#include "litl-core/math.hpp"

namespace LITL::Math
{
    float powf(float base, float exponent) noexcept
    {
        return std::powf(base, exponent);
    }

    float powf(float base, int32_t exponent) noexcept
    {
        return std::powf(base, exponent);
    }

    float powf(float base, uint32_t exponent) noexcept
    {
        return std::powf(base, exponent);
    }

    double powd(double base, double exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    double powd(double base, int32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    double powd(double base, uint32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    uint32_t pow(uint32_t base, uint32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    uint32_t pow(uint32_t base, int32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    int32_t pow(int32_t base, int32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }

    int32_t pow(int32_t base, uint32_t exponent) noexcept
    {
        return std::pow(base, exponent);
    }
}